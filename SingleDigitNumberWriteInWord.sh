#!/bin/bash -x
read -p "Enter a single digit number : " n;
a=1;
b=2;
c=3;
d=4;
e=5;
f=6;
g=7;
h=8;
i=9;
j=0;
if [ $n -eq $a ];
then
	echo "One"
fi
if [ $n -eq $b ];
then
   echo "Two"
fi
if [ $n -eq $c ];
then
	echo "Three"
fi
if [ $n -eq $d ];
then
   echo "Four"
fi
if [ $n -eq $e ];
then
   echo "Five"
fi
if [ $n -eq $f ];
then
   echo "Six"
fi
if [ $n -eq $g ];
then
   echo "Seven"
fi
if [ $n -eq $h ];
then
   echo "Eight"
fi
if [ $n -eq $i ];
then
   echo "Nine"
fi
if [ $n -eq $j ];
then
   echo "Zero"
fi

